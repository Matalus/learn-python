from math import *

print(2)  # whole

print(2.365)  # decimal

print(3 + 4.5)  # addition

print(3 * 10)  # multiplication

print(3 * (4 + 5))  # order of operations

print(10 % 3)  # modulus (remainder of divisibles)

my_num = 5
print(str(my_num))  # cast as string

abs_num = -5
print(abs(abs_num))  # absolute value

print(pow(my_num, 2))  # power of (exponents)

print(min(my_num, 7))  # min function

print(max(my_num, 10))  # max function

print(round(3.7))  # round function

print(floor(5))  # floor

print(ceil(5))  # ceiling

print(sqrt(36))  # square root
